double getSysTickOverflowPeriod(void)
{
	double result = (double)(SysTick->LOAD + 1) / (double)HAL_RCC_GetSysClockFreq();

	int AHBdiv8 = SysTick->CTRL & 0b100;
	if (!AHBdiv8){
		// AHB/8
		result *= 8;
	}
	return result;
}
